/**
 * La enumeración Direccion representa las posibles direcciones en las que puede moverse un objeto
 * en el contexto del juego. Las direcciones incluyen ARRIBA, ABAJO, IZQUIERDA y DERECHA.
 */

public enum Direccion {
    /**
     * Representa la dirección hacia arriba.
     */
    ARRIBA,

    /**
     * Representa la dirección hacia abajo.
     */
    ABAJO,

    /**
     * Representa la dirección hacia la izquierda.
     */
    IZQUIERDA,

    /**
     * Representa la dirección hacia la derecha.
     */
    DERECHA
}
